class Animal {
  constructor() {
    // Tetapkan nilai string「Leo」ke property name
    this.name = "Leo";
    this.age = 3;
    // Tetapkan nilai「3」ke property age
    
  }
}

const animal = new Animal();

// Cetak「Nama: ____」
console.log("Nama: "+animal.name);

// Cetak「Usia: __」
console.log("Usia: "+animal.age);
